public class Calculadora {

    public int a;
    public int b;




    public int somar(int a, int b){
        return a + b;
    }
    public int subtrair(int a, int b){
        return a - b;
    }
    public int multiplicar(int a, int b){
        return a * b;
    }
    public double dividir(double a, double b){
        return a / b;
    }
    public double mode(double a, double b){
        return a % b;
    }

}
